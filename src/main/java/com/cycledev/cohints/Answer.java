package com.cycledev.cohints;

/**
 * Representation of an answer to a {@link Question}
 * 
 * @author leonard
 */
public class Answer {
    
    String questionId;
    String answer;

    public Answer(){
    }
    
    public Answer(String questionId, String answer) {
        this.questionId = questionId;
        this.answer = answer;
    }

    public String getQuestionId() {
        return questionId;
    }

    public void setQuestionId(String questionId) {
        this.questionId = questionId;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    @Override
    public String toString() {
        return "Answer with Question id: " + questionId + 
                " and Answer: \"" + answer + "\"";
    }
    
    
    
}
